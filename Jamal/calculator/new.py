names = ["Jack", "Jay", "Jurgen", "Jamal", "Jurgen", "David", "Thomas", "Martin"]

def func1(array, name):
    newList = []
    newList.insert(0, name)
    for i in array:
        newList.append[i]
        newList.append(name)
        
    return newList

